class FraudRule(TimeStampedModel):
    """Configurable fraud detection rules."""

    RULE_VELOCITY = 'velocity'
    RULE_AMOUNT = 'amount'
    RULE_GEO = 'geo'
    RULE_DEVICE = 'device'
    RULE_PROMO = 'promo'
    RULE_PAYMENT = 'payment'

    RULE_TYPE_CHOICES = [
        (RULE_VELOCITY, 'Velocity Check'),
        (RULE_AMOUNT, 'Amount Anomaly'),
        (RULE_GEO, 'Geo Mismatch'),
        (RULE_DEVICE, 'Device Fingerprint'),
        (RULE_PROMO, 'Promo Abuse'),
        (RULE_PAYMENT, 'Payment Pattern'),
    ]

    ACTION_FLAG = 'flag'
    ACTION_BLOCK = 'block'
    ACTION_VERIFY = 'verify'
    ACTION_LOG = 'log'

    ACTION_CHOICES = [
        (ACTION_FLAG, 'Flag for Review'),
        (ACTION_BLOCK, 'Block Transaction'),
        (ACTION_VERIFY, 'Require Verification'),
        (ACTION_LOG, 'Log Only'),
    ]

    name = models.CharField(max_length=100)
    rule_type = models.CharField(max_length=20, choices=RULE_TYPE_CHOICES)
    threshold = models.DecimalField(max_digits=10, decimal_places=2, default=Decimal('0'))
    window_hours = models.PositiveIntegerField(default=24)
    action = models.CharField(max_length=10, choices=ACTION_CHOICES, default=ACTION_FLAG)
    score_impact = models.IntegerField(default=20, help_text='Risk score impact (0-100)')
    is_enabled = models.BooleanField(default=True)
    description = models.TextField(blank=True)

    class Meta:
        app_label = 'core'

    def __str__(self):
        return f"FraudRule({self.name}, {self.rule_type})"


class FraudAlert(TimeStampedModel):
    """Individual fraud alert generated by the fraud engine."""

    SEVERITY_LOW = 'low'
    SEVERITY_MEDIUM = 'medium'
    SEVERITY_HIGH = 'high'
    SEVERITY_CRITICAL = 'critical'

    SEVERITY_CHOICES = [
        (SEVERITY_LOW, 'Low'),
        (SEVERITY_MEDIUM, 'Medium'),
        (SEVERITY_HIGH, 'High'),
        (SEVERITY_CRITICAL, 'Critical'),
    ]

    STATUS_OPEN = 'open'
    STATUS_INVESTIGATING = 'investigating'
    STATUS_RESOLVED = 'resolved'
    STATUS_FALSE_POSITIVE = 'false_positive'

    STATUS_CHOICES = [
        (STATUS_OPEN, 'Open'),
        (STATUS_INVESTIGATING, 'Investigating'),
        (STATUS_RESOLVED, 'Resolved'),
        (STATUS_FALSE_POSITIVE, 'False Positive'),
    ]

    user = models.ForeignKey(
        settings.AUTH_USER_MODEL, on_delete=models.SET_NULL,
        null=True, blank=True, related_name='fraud_alerts',
    )
    rule = models.ForeignKey(FraudRule, on_delete=models.SET_NULL, null=True, blank=True)
    alert_type = models.CharField(max_length=30)
    severity = models.CharField(max_length=10, choices=SEVERITY_CHOICES, default=SEVERITY_MEDIUM)
    status = models.CharField(max_length=20, choices=STATUS_CHOICES, default=STATUS_OPEN)
    risk_score = models.IntegerField(default=0)
    description = models.TextField(blank=True)
    evidence = models.JSONField(default=dict, blank=True)
    ip_address = models.GenericIPAddressField(null=True, blank=True)
    device_fingerprint = models.CharField(max_length=255, blank=True)
    booking_id = models.IntegerField(null=True, blank=True)
    transaction_id = models.CharField(max_length=100, blank=True)
    resolved_by = models.ForeignKey(
        settings.AUTH_USER_MODEL, on_delete=models.SET_NULL,
        null=True, blank=True, related_name='resolved_fraud_alerts',
    )
    resolved_at = models.DateTimeField(null=True, blank=True)
    resolution_note = models.TextField(blank=True)

    class Meta:
        app_label = 'core'
        ordering = ['-created_at']
        indexes = [
            models.Index(fields=['user', 'status', '-created_at'], name='fraud_user_status_idx'),
            models.Index(fields=['severity', 'status'], name='fraud_severity_status_idx'),
        ]

    def __str__(self):
        return f"FraudAlert({self.alert_type}, {self.severity}, user={self.user_id})"


